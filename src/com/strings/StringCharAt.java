package com.strings;

public class StringCharAt {
	public static void main(String[] args) {

		String s = "sairam";
		System.out.println(s.charAt(2));
		char ch = "ratan".charAt(2);
		System.out.println(ch);
		String s1 = "subbu javadeveloper";
		String[] s2 = s1.split(" ");
		for (String s3 : s2) {
			System.out.println(s3);
		}

	}

}
